function rgbToHex(r: number, g: number, b: number): string {
  const toHex = (n: number) => {
    const val = Math.round(n * 255);
    return val.toString(16).padStart(2, '0').toUpperCase();
  };
  return `#${toHex(r)}${toHex(g)}${toHex(b)}`;
}

